static void	init_keys(t_env *e)
{
	e->keys[KEY_W] = 0;
	e->keys[KEY_A] = 0;
	e->keys[KEY_S] = 0;
	e->keys[KEY_D] = 0;
	e->keys[KEY_LEFT] = 0;
	e->keys[KEY_RIGHT] = 0;
}

int	main(void)
{
	t_env	e;

	e.mlx = mlx_init();
	e.win = mlx_new_window(e.mlx, SCREEN_WIDTH, SCREEN_HEIGHT, "Raycaster");
	e.img = mlx_new_image(e.mlx, SCREEN_WIDTH, SCREEN_HEIGHT);
	e.data = (int *)mlx_get_data_addr(e.img, &e.bpp, &e.sizeline, &e.endian);
	e.pos_x = 15;
	e.pos_y = 15;
	e.dir_x = -1;
	e.dir_y = 0;
	e.plane_x = 0;
	e.plane_y = 0.66;
	e.map_height = MAP_HEIGHT;
	e.map_width = MAP_WIDTH;
	init_keys(&e);
	mlx_hook(e.win, 2, 1L << 0, key_press, &e);
	mlx_hook(e.win, 3, 1L << 1, key_release, &e);
	mlx_loop_hook(e.mlx, render_scene, &e);
	mlx_loop(e.mlx);
	return (0);
}
